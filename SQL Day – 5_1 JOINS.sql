--58. FIND DEPARTMENTS WHERE THE MANAGER EARNS LESS THAN THE AVERAGE SALARY OF THE EMPLOYEES IN THAT DEPARTMENT, AND LIST THE DIFFERENCE IN SALARY.
SELECT 
    d.DepartmentID, 
    d.DepartmentName, 
    e.FirstName AS ManagerFirstName, 
    e.LastName AS ManagerLastName, 
    e.Salary AS ManagerSalary, 
    (AVG(emp.Salary) - e.Salary) AS SalaryDifference
FROM Departments d
JOIN Employee e ON d.ManagerID = e.EmployeeID
JOIN Employee emp ON d.DepartmentID = emp.DepartmentID
GROUP BY d.DepartmentID, d.DepartmentName, e.EmployeeID, e.FirstName, e.LastName, e.Salary
HAVING e.Salary < AVG(emp.Salary);


