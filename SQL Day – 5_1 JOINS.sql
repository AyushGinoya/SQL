--56. LIST THE DEPARTMENTS WHERE THE TOTAL SALARY EXPENDITURE ON EMPLOYEES IS HIGHER THAN THE AVERAGE SALARY EXPENDITURE ACROSS ALL DEPARTMENTS.
WITH DeptSalary AS (
    SELECT DepartmentID, SUM(Salary) AS TotalSalary
    FROM Employee
    GROUP BY DepartmentID
),
AvgSalary AS (
    SELECT AVG(TotalSalary) AS AvgSalary
    FROM DeptSalary
)
SELECT d.DepartmentID, d.DepartmentName, ds.TotalSalary
FROM Departments d
JOIN DeptSalary ds ON d.DepartmentID = ds.DepartmentID
WHERE ds.TotalSalary > (SELECT AvgSalary FROM AvgSalary);



